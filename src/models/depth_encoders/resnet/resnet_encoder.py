class ResnetEncoderDecoder(nn.Module):
    def __init__(self, num_layers=50, num_features=256, model_dim=32):# model_dim=128
        super(ResnetEncoderDecoder, self).__init__()
        self.encoder = ResnetEncoder(num_layers = num_layers, pretrained = True, num_input_images=1)
        self.decoder = DecoderBN(num_features=num_features, num_classes=model_dim, bottleneck_features=2048)
    def forward(self, x, **kwargs):
        x = self.encoder(x)
        return self.decoder(x, **kwargs)

    def from_pretrained(self, weights_path, device='cpu'):
        loaded_dict_enc = torch.load(weights_path, map_location=device)
        filtered_dict_enc = {k: v for k, v in loaded_dict_enc.items() if k in self.state_dict()}
        self.load_state_dict(filtered_dict_enc)
        self.eval()

